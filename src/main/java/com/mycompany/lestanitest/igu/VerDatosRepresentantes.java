    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // Control de q la tabla no este vacia
        if (tablaRepresentante.getRowCount() > 0) {
            //validar q se haya seleccionado un registro
            if (tablaRepresentante.getSelectedRow() != -1) {
                //obtener la id de lo q quiero editar
                int idRepresentante = Integer.parseInt(String.valueOf(tablaRepresentante.getValueAt(tablaRepresentante.getSelectedRow(), 0)));
                EditarRepresentante editar = new EditarRepresentante(idRepresentante);
                editar.setVisible(true);
                editar.setLocationRelativeTo(null);
                this.dispose();
            } else {
                mostrarMensaje("No seleccinÃ³ un Registro para editar", "Error", "Error al editar");
            }
        } else {
            mostrarMensaje("La tabla esta vacia, no se puede editar", "Error", "Error al editar");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // Control de q la tabla no este vacia
        if (tablaRepresentante.getRowCount() > 0) {
            //validar q se haya seleccionado un registro
            if (tablaRepresentante.getSelectedRow() != -1) {
                //obtener la id de lo q quiero borrar
                int idRepresentante = Integer.parseInt(String.valueOf(tablaRepresentante.getValueAt(tablaRepresentante.getSelectedRow(), 0)));

                control.borrarRepresentante(idRepresentante);
                mostrarMensaje("Representante borrado correctamente", "Info", "Borrado con exito!");
                mostrarTablaRepresentante();
            } else {
                mostrarMensaje("No seleccinÃ³ un Registro para eliminar", "Error", "Error al eliminar");
            }
        } else {
            mostrarMensaje("La tabla esta vacia, no se puede eliminar", "Error", "Error al eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

