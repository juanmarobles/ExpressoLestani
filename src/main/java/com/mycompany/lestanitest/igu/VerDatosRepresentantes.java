    private void mostrarTablaRepresentante() {
        //filas y columnas no editables
        DefaultTableModel tabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        //nombres de columnas
        String titulos[] = {"Id_Representante", "Nombre", "Localidad", "Direccion", "Telefono"};
        tabla.setColumnIdentifiers(titulos);

        //carga de los datos desde la bd
        List<Representantes> listaRepresentantes = control.traerRepresentantes();

        //recorrer lista y mostrar elementos en la tabla
        if (listaRepresentantes != null) {
            for (Representantes r : listaRepresentantes) {
                Object[] objeto = {r.getId(), r.getNombre(), r.getLocalidad(), r.getDireccion(), r.getTelefono()};

                tabla.addRow(objeto);
            }
        }
        tablaRepresentante.setModel(tabla);
    }

